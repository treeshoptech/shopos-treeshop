// Helper functions
export function getLocationBySlug(slug: string): Location | undefined {
  return locations.find(l => l.slug === slug)
}

export function getLocationsByCounty(county: string): Location[] {
  return locations.filter(l => l.county === county)
}

export function getLocationsByTier(tier: 1 | 2 | 3): Location[] {
  return locations.filter(l => l.tier === tier)
}

export function getNearbyLocations(slug: string): Location[] {
  const location = getLocationBySlug(slug)
  if (!location) return []
  return location.nearbyAreas
    .map(s => getLocationBySlug(s))
    .filter((l): l is Location => l !== undefined)
}

