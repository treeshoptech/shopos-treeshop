'use client'

import Script from 'next/script'
import { usePathname, useSearchParams } from 'next/navigation'
import { useEffect, Suspense } from 'react'

const PIXEL_ID = '1644359313216151'

declare global {
  interface Window {
    fbq: any
    _fbq: any
  }
}

function MetaPixelPageView() {
  const pathname = usePathname()
  const searchParams = useSearchParams()

  useEffect(() => {
    if (typeof window.fbq === 'function') {
      window.fbq('track', 'PageView')
    }
  }, [pathname, searchParams])

  return null
}

export default function MetaPixel() {
  return (
    <>
      <Script
        id="meta-pixel"
        strategy="afterInteractive"
        dangerouslySetInnerHTML={{
          __html: `
            !function(f,b,e,v,n,t,s)
            {if(f.fbq)return;n=f.fbq=function(){n.callMethod?
            n.callMethod.apply(n,arguments):n.queue.push(arguments)};
            if(!f._fbq)f._fbq=n;n.push=n;n.loaded=!0;n.version='2.0';
            n.queue=[];t=b.createElement(e);t.async=!0;
            t.src=v;s=b.getElementsByTagName(e)[0];
            s.parentNode.insertBefore(t,s)}(window, document,'script',
            'https://connect.facebook.net/en_US/fbevents.js');
            fbq('init', '${PIXEL_ID}');
            fbq('track', 'PageView');
          `,
        }}
      />
      <noscript>
        <img
          height="1"
          width="1"
          style={{ display: 'none' }}
          src={`https://www.facebook.com/tr?id=${PIXEL_ID}&ev=PageView&noscript=1`}
          alt=""
        />
      </noscript>
      <Suspense fallback={null}>
        <MetaPixelPageView />
      </Suspense>
    </>
  )
}

export const trackEvent = (eventName: string, params?: object) => {
  if (typeof window !== 'undefined' && typeof window.fbq === 'function') {
    window.fbq('track', eventName, params)
  }
}

export const trackCustomEvent = (eventName: string, params?: object) => {
  if (typeof window !== 'undefined' && typeof window.fbq === 'function') {
    window.fbq('trackCustom', eventName, params)
  }
}

export const MetaEvents = {
  Lead: (value?: number) => trackEvent('Lead', value ? { value, currency: 'USD' } : undefined),
  CompleteRegistration: (value?: number) => trackEvent('CompleteRegistration', value ? { value, currency: 'USD' } : undefined),
  ViewContent: (contentName: string, contentCategory: string) =>
    trackEvent('ViewContent', { content_name: contentName, content_category: contentCategory }),
  Search: (searchString: string) => trackEvent('Search', { search_string: searchString }),
  InitiateCheckout: (value: number, contentName: string) =>
    trackEvent('InitiateCheckout', { value, currency: 'USD', content_name: contentName }),
  Purchase: (value: number, contentName: string) =>
    trackEvent('Purchase', { value, currency: 'USD', content_name: contentName }),
  Contact: () => trackEvent('Contact'),
  QuoteStarted: (service: string) => trackCustomEvent('QuoteStarted', { service }),
  QuoteCompleted: (service: string, value: number) => trackCustomEvent('QuoteCompleted', { service, value, currency: 'USD' }),
  ToolUsed: (toolName: string) => trackCustomEvent('ToolUsed', { tool_name: toolName }),
  FoundingMemberApply: () => trackCustomEvent('FoundingMemberApply', { value: 30000, currency: 'USD' }),
}
